/**
 * Tipos de datos
 */

// numero - number
console.log(27);
console.log(27.56);

// texto - string
console.log("Julian Millar");
console.log("Julian Millar");
console.log(`Hola, soy Julian`);

// logicos - booleans
console.log(true);
console.log(false);

// indefinido - undefined
console.log(undefined);

// null
console.log(null);

/**
 * Tipos de datos estructurales
 */

// Objeto - object
console.log({
  nombre: "Julian",
  edad: 27,
  fechaDeNacimiento: null,
  esAlto: false,
  ciudades: ["Quito", "Guayaquil", "Bueno Aires"],
  direccion: { callePrincipal: "Balmaceda" },
});

// Arreglo - dato de tipo lista - array
console.log(["Mexico", "Chile", "Venezuela"]);
console.log([21, true, { nombre: "Gabriel" }, [1, 2, 3, 4]]);
